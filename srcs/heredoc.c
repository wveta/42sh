#include "../includes/minishell.h"

char	*ft_clear_quotes(char *s)
{
	int		i;
	char	c;

	if (s)
	{
		i = 0;
		c = '\0';
		while (s[i])
		{
			if (s[i] == '"' || s[i] == '\'')
			{
				if (c == s[i])
					c = '\0';
				else if (c == '\0')
					c = s[i];
				if (c == '\0' || c == s[i])
					ft_strcpy(s + i, s + i + 1);
			}
			i++;
		}
	}
	return (s);
}
